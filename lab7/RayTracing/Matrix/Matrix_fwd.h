#pragma once

/*
Объявления классов матриц для тех случае, когда полное определение класса не требуется
*/

template <class T>
class CMatrix3;

typedef CMatrix3<double> CMatrix3d;
typedef CMatrix3<float> CMatrix3f;


template <class T>
class CMatrix4;

typedef CMatrix4<double> CMatrix4d;
typedef CMatrix4<float> CMatrix4f;

